package io.vertx.ext.jpa;

import io.vertx.codegen.annotations.GenIgnore;
import io.vertx.codegen.annotations.VertxGen;
import io.vertx.core.AsyncResult;
import io.vertx.core.Handler;
import io.vertx.core.json.JsonArray;
import io.vertx.core.json.JsonObject;
import io.vertx.ext.jpa.impl.QueryImpl;

@VertxGen
public interface Query extends javax.persistence.Query {

  @GenIgnore
  public static Query create(javax.persistence.Query object) {

    return new QueryImpl(object);
  }

  void getResultList(Handler<AsyncResult<JsonArray>> resultHandler);

  void getSingleResult(Handler<AsyncResult<JsonObject>> resultHandler);

  void executeUpdate(Handler<AsyncResult<Integer>> resultHandler);

}